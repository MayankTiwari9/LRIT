// Counting from 0 to 9
console.log("Counting from 0 to 9");

for (let i = 0; i < 10; i++) {
  console.log(i);
}

// Counting backward from 5 to 1
console.log("Counting backward from 5 to 1");

for (let i = 5; i > 0; i--) {
  console.log(i);
}

// Printing even numbers between 1 and 10:

console.log("Printing even numbers between 1 and 10:");

for (let i = 2; i <= 10; i += 2) {
  console.log(i);
}

// Looping through an array:

console.log('Looping through an array');

const fruits = ['apple', 'banana', 'orange', 'grape'];

for (let i = 0; i < fruits.length; i++) {
  console.log(fruits[i]);
}
