// String
let single_quote = 'Hello World'; // In single quotes

let double_quote = "Hello World"; // In double quotes

console.log(single_quote);
console.log(double_quote);

// Numbers
let intNum = 10; // Integer

let decNum = 5.63; //Decimal numbers

console.log(intNum);
console.log(decNum);

// Boolean
let isValid = true;

let isStudent = false;

console.log(isValid);
console.log(isStudent);

// Undefined
let name;

console.log(name);

// Null
let empty = null;

console.log(empty);

// Symbol
let sym1 = Symbol("Hello World!");
let sym2 = Symbol("Hello World!");
console.log("These two Symbols are the same:", sym1 === sym2);