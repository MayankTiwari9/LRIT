let x = 20;
let y = 10;

console.log(x > y);   // Greater than
// Retursn true

console.log(x < y);   // Less than
// Retursn false

console.log(x >= y);  // Greater than or equal to
// Retursn true

console.log(x <= y);  // Less than or equal to
// Retursn false

console.log(x === y); // Equal to (strict)
// Retursn false

console.log(x !== y); // Not equal to (strict)
// Retursn true

