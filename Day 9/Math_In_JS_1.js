// Math.round
let a = 4.6;

console.log(Math.round(a)); // 5

// Math.ceil
let b = 4.7;

console.log(Math.ceil(b)); // 5

// Math.floor()
let c = 10.8;

console.log(Math.floor(c)); // 10

// Math.pow()
let x = 5;
let y = 2;

console.log(Math.pow(x, y)); // 25

// Math.sqrt()
let d = 121;

console.log(Math.sqrt(d)); // 11

// Math.abs()
let e = -6.2;

console.log(Math.abs(e)); // 6.2
