// for Loop

console.log('for Loop');

for (let i = 0; i < 5; i++) {
    console.log(i);
  }

  
// while Loop

console.log("while Loop");

let i = 0;
while (i < 5) {
  console.log(i);
  i++;
}


// do-while loop

console.log("do-while loop");

let j = 0;
do {
  console.log(j);
  j++;
} while (j < 5);

