// toString()
let num = 100;
let strNum = num.toString();

console.log(strNum); // "100"

// toFixed()
let nums = 3.14159;
let fixedNum = nums.toFixed(2);

console.log(fixedNum); // 3.14

// parseInt()
let strNums = "50";
let intNum = parseInt(strNums);

console.log(intNum); // 50

// parseFloat()
let stringNumber = "3.14";
let floatNum = parseFloat(stringNumber);

console.log(floatNum); // 3.14
